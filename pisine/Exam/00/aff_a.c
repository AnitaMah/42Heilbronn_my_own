#include <unistd.h>

int	main(int argc, char **argv)
{
	int	j = 0;

	if (argc > 1)
	{
		while (argv[1][j] != '\0')
		{
			if (argv[1][j] == 'a')
			{
				write(1, "a\n", 2);
				break ;
			}
			j++;
		}
	}
	write(1, "a\n", 2);
}
