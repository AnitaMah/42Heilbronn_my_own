#include <unistd.h>

int	main(int argv, char **argc)
{
	int	i;

	i = 1;
	while (i < argv)
	{
		while (*argc[i])
		{
			write(1, argc[i]++, 1);
		}
		write(1, "\n", 1);
		i++;
	}
}
